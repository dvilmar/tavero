export function detectLocale(headers: Headers): Locale {
  const acceptLang = headers.get('accept-language') ?? ''
  if (acceptLang.toLowerCase().startsWith('en')) return 'en'
  return 'es'
}

export function t(locale: Locale, key: string, params?: Record<string, unknown>): string {
  const raw = messages[locale]?.[key] ?? messages.es[key] ?? key
  if (!params) return raw
  return raw.replace(/\{(\w+)\}/g, (_, k) => String(params[k] ?? ''))
}
